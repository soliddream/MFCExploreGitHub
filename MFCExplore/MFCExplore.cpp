int CMFCExploreApp::ConnectDatabase(LPCTSTR lpstrConnection)
{
	try
	{
		if (m_pDB==NULL)
		{
			m_pDB = new CADODatabase();
			m_pDB->Open(lpstrConnection);
		}
		
	}
	catch (CADOException &e)
	{
		TRACE(e.GetErrorMessage());
	}

	return 0;
}


int CMFCExploreApp::CloseDatabase()
{
	if (m_pDB!=NULL)
	{
		m_pDB->Close();
		delete m_pDB;
		m_pDB = NULL;
	}
	return 0;
}
